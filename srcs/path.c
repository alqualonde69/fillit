#include "fillit.h"

int		path(char **ttr, int i, int j)
{
	int		x;
	int		y;
	int		p;

	x = 0;
	y = 0;
	p = 0;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
		{
			if (ttr[i][j] == '#' && !p)
			{
				p = (x + 2) * 10 + (y + 2);
				x = i;
				y = j;
			}
			else if (ttr[i][j] == '#' && p)
				p = (p * 10 + (i - x + 2)) * 10 + (j - y + 2);
		}
	}
	return (p);
}
