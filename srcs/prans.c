#include "fillit.h"

void	prans(char **fld)
{
	int		i;
	int		j;

	i = -1;
	while (fld[++i])
	{
		j = -1;
		if (fld[i][0] == '\0')
			break ;
		if (fld[i][0] != '\0' && i > 0)
			write(1, "\n", 1);
		while (fld[i][++j])
			write(1, &fld[i][j], 1);
	}
	write(1, "\n", 1);
}
