#include "fillit.h"

void	pth(t_flist **fi, char **ttr)
{
	int		p;

	p = path(ttr, -1, -1);
	(*fi)->i1 = p / 100000 % 10 - 2;
	(*fi)->j1 = p / 10000 % 10 - 2;
	(*fi)->i2 = p / 1000 % 10 - 2;
	(*fi)->j2 = p / 100 % 10 - 2;
	(*fi)->i3 = p / 10 % 10 - 2;
	(*fi)->j3 = p % 10 - 2;
}
