#include "fillit.h"

void	fr_tf(char **ttr, t_flist *fi, int i)
{
	t_flist	*t;

	fr_ttr(ttr, i);
	while (fi)
	{
		t = fi;
		fi = fi->next;
		free(t);
	}
}
