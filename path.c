#include "fillit.h"

void	path(char **ttr, int i, int j, t_flist **n)
{
	int		x;
	int		y;
	int		p;

	x = 0;
	y = 0;
	p = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
		{
			if (ttr[i][j] == '#' && p == -1)
			{
				p = 0;
				x = i;
				y = j;
			}
			else if (ttr[i][j] == '#' && p >= 0)
			{
				(*n)->c[p++] = i - x;
				(*n)->c[p++] = j - y;
			}
		}
	}
}
