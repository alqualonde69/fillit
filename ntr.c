#include "fillit.h"

void	ntr(char *buf, char **ttr)
{
	t_flist	*fi;
	int		a[3];
	char	l;

	fi = NULL;
	l = 'A';
	a[0] = 0;
	while (buf[a[0]])
	{
		a[1] = -1;
		while (++a[1] < 4)
		{
			a[2] = -1;
			while (++a[2] < 4)
				ttr[a[1]][a[2]] = buf[a[0]++];
			ttr[a[1]][a[2]] = '\0';
			(buf[a[0]] == '\n' ? a[0]++ : a[0]);
		}
		if (!(nfi(ttr, &fi, &l)))
			return (fr_tf(ttr, &fi, 4));
		buf[a[0]] == '\n' ? a[0]++ : a[0];
	}
	fr_ttr(ttr, 4);
	ans(&fi, l);
}
