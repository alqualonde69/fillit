#include "fillit.h"

int		nfi(char **ttr, t_flist **fi, char *a)
{
	char l;

	l = *a;
	if (!(valsh(ttr)))
	{
		error();
		return (0);
	}
	if (strc(fi, ttr, l))
	{
		*a = l + 1;
		return (1);
	}
	return (0);
}
