#include "fillit.h"

int		pref(t_flist *fi, int i)
{
	int		c[6];
	int		k;

	k = -1;
	while (++k < 6)
		c[k] = fi->c[k];
	while ((fi = fi->prev))
		if (fi->c[0] == c[0] && fi->c[1] == c[1] && fi->c[2] == c[2] &&
				fi->c[3] == c[3] && fi->c[4] == c[4] && fi->c[5] == c[5])
			return (i <= fi->i ? (fi->i * 10) + fi->j : 0);
	return (0);
}
